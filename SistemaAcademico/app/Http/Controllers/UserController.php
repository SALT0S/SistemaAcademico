    public function getusers()
    {
        $users = User::all();

        // Modificar el estado de los usuarios
        foreach ($users as $user) {
            // Realizar la manipulación de datos aquí
            // Por ejemplo, convertir el estado a mayúsculas
            $user->estado = strtoupper($user->estado);
            if ($user->estado == 1) {
                $user->estado = '<span class="badge badge-success" style="background: green"> Activo</span>';

            } else {
                $user->estado = '<span class="badge badge-danger" style="background: orange"> Inactivo</span>';

            }
        }

        return response()->json($users);
    }
